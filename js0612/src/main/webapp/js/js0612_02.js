// 한줄 주석

// 변수

var num = 100;
var str = "200.3";
document.write(num+str,"<br>");

var num2 = parseInt(str);// parseInt(정수)로 형변환
var num4 = parseFloat(str);// parseFloat(실수)로 형변환
var num3 = Number(str)// Number(정수+실수)로 형변환
document.write(num+num2,"<br>");
document.write(num+num3,"<br>");
document.write(num+num4,"<br>");